package atividade;

public class exercicio108 {
	
	public static void main(String[] args) {
		
		int nota1 = 90;
		int nota2 = 90;
		int nota3 = 100;
		int nota4 = 80;
		
		if(nota1 == nota2) {
			
			System.out.println("Iguais");
		}
		
		else {
			System.out.println("Diferentes");
		}
		
		if(nota3 != nota4) {
			System.out.println("Diferente");
		}
		else {
			System.out.println("iguais");
		}
		
		if(nota1 > nota4) {
			System.out.println("Maior");
		}
		else {
			System.out.println("Menor");
		}
		
		if(nota4 < nota3) {
			System.out.println("Menor");
		}
		else {
			System.out.println("Maior");
		}
		
		
	}
	

}
